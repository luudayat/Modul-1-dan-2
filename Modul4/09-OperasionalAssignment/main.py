# Operasional yang dapat dilakukan dengan penyingkatan
# Operasio di tambah dengan assignment

a = 5 # adalah assignment
print("nilai a =" ,a)

a += 5 # artinya adalah a = a + 1
print("nilai a += 1, nilai a menjadi" ,a)

a -= 5 # artinya adalah a = a - 1
print("nilai a -= 1, nilai a menjadi" ,a)

a *= 5 # artinya adalah a = a * 1
print("nilai a *= 1, nilai a menjadi" ,a)

a /= 5 # artinya adalah a = a / 1
print("nilai a /= 1, nilai a menjadi" ,a)

b = 10
print("/nnilai b =" ,b)

